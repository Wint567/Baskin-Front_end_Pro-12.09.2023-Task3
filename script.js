var num1 = parseFloat(prompt("Введіть перше число:"));
var num2 = parseFloat(prompt("Введіть друге число:"));

alert(num1 + " + " + num2 + " = " + (num1 + num2));
alert(num1 + " - " + num2 + " = " + (num1 - num2));
alert(num1 + " * " + num2 + " = " + (num1 * num2));
alert(num1 + " / " + num2 + " = " + (num1 / num2));